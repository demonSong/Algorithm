	class Edge{
		int from;
		int to;
		double rate;
		
		Edge(int from, int to, double rate){
			this.from = from;
			this.to   = to;
			this.rate = rate;
		}
	}
	
	List<Edge> edges;
	boolean bellmanFord(int s, int n) {
		double[] d = new double[n];
		Arrays.fill(d, 0);
		d[s] = 1;
		boolean ok = false;
		for (int i = 0; i < n; ++i) {
			for (Edge e : edges) {
				double nm = d[e.from] * e.rate;
				if (nm > d[e.to]) {
					d[e.to] = nm;
					if (i == n - 1) {
						ok = true;
					}
				}
			}
		}
		return ok;
	}
	
	void read() {
		int T = 0;
		while (true) {
			int n = ni();
			if (n == 0) break;
			edges = new ArrayList<Edge>();
			
			Map<String, Integer> map = new HashMap<String, Integer>();
			for (int i = 0; i < n; ++i) {
				String ns = ns();
				map.put(ns, i);
			}
			
			
			int M = ni();
			for (int i = 0; i < M; ++i) {
				String from = ns();
				double rate = nd();
				String to   = ns();
				edges.add(new Edge(map.get(from), map.get(to), rate));
			}
			
			if (bellmanFord(0, n)){
				out.println("Case " + (++T) + ": Yes");
			}
			else {
				out.println("Case " + (++T) + ": No");
			}
		}
	}

