	void merge(int s, int e) {
		if (e - s <= 1) return;
		int m = (s + e) / 2;
		merge(s, m);
		merge(m, e);
		for (int i = s, j = m; i < m; ++i) {
			while (j < e && num[i] > num[j]) j ++;
			count += j - m;
		}
		
		mergeSort(s, e);
	}
	
	void mergeSort(int s, int e) {
		int m = (s + e) / 2;
		long[] aux = new long[e - s];
		int i = s;
		int j = m;
		int k = 0;
		
		while (i < m && j < e) {
			if (num[i] < num[j]) aux[k++] = num[i++];
			else aux[k++] = num[j++];
		}
		
		while (i < m) aux[k++] = num[i++];
		while (j < e) aux[k++] = num[j++];
		
		for (int t = 0, l = s; l < e; ++l) num[l] = aux[t++];
	}
	
	void read() {
		while (true) {
			N = ni();
			if (N == 0) break;
			
			num = new long[N];
			for (int i = 0; i < N; ++i) {
				num[i] = ni();
			}
			
			count = 0;
			merge(0, N);
			out.println(count);
		}
	}

